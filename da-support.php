<h2>Crypto-coins</h2>

<p id="cryptocoin">Krypto-mønter (eller mere internatinalt: Crypto-coins) er en slags digitale kontanter baseret på en open-source, peer-to-peer krypteret protokol først beskrevet i 2009 med introduktionen af Bitcoin (BTC) skabt af det mystiske psudonym Satoshi Nakamoto. Krypto-mønter er ikke styret af nogen regering eller central myndighed, men eksisterer på et internet-baseret P2P netwærk der registrerer alle transaktionerne.</p>
<p>Hvis du ejer bitcoins eller andre af de kryptomønter der er vist herunder, vil forfatteren gladeligt acceptere et tilskud da han er meget interesseret i BLOCK-CHAIN-teknologien for tiden. Hvis du er inde i den slags ting ved du sandsynligvis hvordan man gør.</p>

